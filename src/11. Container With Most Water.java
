// time complexity (O(n))

class Solution {
    public int maxArea(int[] height) {
        int res = Integer.MIN_VALUE;
        int len = height.length;
        int left = 0, right = len - 1;
        while (left < right) {
            res = Math.max(res, Math.min(height[left], height[right]) * (right - left));
            if (height[left] < height[right]) {
                left++;
            } else {
                right--;
            }
        }
        return res;
    }
}