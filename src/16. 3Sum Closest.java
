/* 
1. Traverse the array to find the first number
2. Use two pointers left and right to locate, use diff to store the difference
3. if sum < target: right-- else: left++
*/

class Solution {
    public int threeSumClosest(int[] nums, int target) {
        int res = nums[0] + nums[1] + nums[2];
        int diff = Math.abs(res -target);
        Arrays.sort(nums);
        
        for (int i = 0; i < nums.length - 2; i++) {
            int left = i + 1, right = nums.length - 1;
            while (left < right) {
                int sum = nums[i] + nums[left] + nums[right];
                int newDiff = Math.abs(sum - target);
                if (newDiff < diff) {
                    diff = newDiff;
                    res = sum;
                }
                if (sum < target) {
                    left++;
                } else {
                    right--;
                } 
            }
        }
        return res;
    }
}